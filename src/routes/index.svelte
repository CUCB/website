<h1>Welcome to CUCB!</h1>

<p>
  The Cambridge University Ceilidh Band is a thriving non-audition society of energetic musicians brought together by
  our inexhaustible enthusiasm for folk music. While the society's origins are in folk tunes from the British Isles, our
  interests have expanded and we now perform a wider variety of folk music.
</p>

<p>
  We are
  <a href="/book">always available for ceilidhs</a>
  , May Balls, weddings, birthday parties, or any other events which might benefit from our talent and energy. With a
  rotating lineup, we play at over 70 events a year, mainly around Cambridge. Since we are so active, we, and our
  associated callers, have a wealth of experience to offer in creating the perfect atmosphere for an fantastic evening's
  dancing, or perhaps simply livening up a social occasion. If this sounds interesting, we look forward to hearing from
  you!
</p>

<p>
  On the other hand, if you like the idea of getting involved in the band and would be interested in
  <a href="/join">joining us</a>
  , then you are warmly encouraged to
  <a href="/faqs/join">browse the information</a>
  on the site, join our
  <a href="/mailinglists">mailing list</a>
  and come along to a rehearsal!
</p>

