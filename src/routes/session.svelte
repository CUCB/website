<h1>Sessions</h1>

<p>
  For some members of CUCB, one of the most enjoyable parts of being in the band is heading to the pub to have a drink,
  a chat and a bit of a traditional music session.
</p>

<ul>
  <li>
    On
    <b>Thursday evenings</b>
    in term, after the weekly rehearsal which finishes at 10PM, we're currently heading to The Anchor. This can most
    easily be found by following the trail of people heading over after a rehearsal - but it's best to come along to
    rehearsal or contacting someone there to make sure you know where people are going on any given evening!
  </li>
  <li>
    We may run sessions on
    <b>Saturday or Sunday afternoons</b>
    , but there is currently no fixed weekly slot so keep your eyes on the mailing lists for these sessions as they
    come!
  </li>
</ul>

<p>
  Make sure you're on the
  <a href="/mailinglists/">chat mailing list</a>
  to get e-mails about sessions!
</p>

<p>
  It's a chance for a bit of informal music making - generally someone will start a tune and others will start to join
  in, play accompaniment, or start to pick it out by ear. Maybe some one else will start a tune, or someone will sing a
  song, and there'll be an eclectic mix of music from around the British Isles and beyond.
</p>

<p>
  Don't worry if you've never played in a session before, or aren't used to playing without the sheet music. Nobody's
  going to mind if you take a peek at the dots, and you'll probably find you get swept along and pick up more than you'd
  expect of the jigs, reels and whatever else gets thrown in the mix!
</p>
<CaptionedImage src="/static/images/session/scotland-boat.jpg" alt="People playing instruments in a session on a boat">
  Some people playing in a session on tour in Scotland
</CaptionedImage>
<p>
  We know that sessions can be a little scary to begin with, and we try hard to make ours as welcoming as possible. For
  many of our members, both new and old to traditional music, they are the highlight of the CUCB calendar. Please come
  along to see one for yourself, and feel free to
  <a href="/committee/">contact a member of the committee</a>
  with any questions you might have.
</p>
